import { readFile } from "fs/promises";
import path from "path";
import type { ActivityEntry } from "@/lib/radicle";

type SnapshotFile = {
  entries?: unknown[];
};

function isActivityEntry(x: unknown): x is ActivityEntry {
  if (!x || typeof x !== "object") return false;
  const o = x as Record<string, unknown>;
  const c = o.commit;
  if (!c || typeof c !== "object") return false;
  const commit = c as Record<string, unknown>;
  const committer = commit.committer;
  if (!committer || typeof committer !== "object") return false;
  const t = (committer as { time?: unknown }).time;
  return (
    typeof o.rid === "string" &&
    typeof o.repoName === "string" &&
    typeof commit.id === "string" &&
    typeof commit.summary === "string" &&
    typeof t === "number"
  );
}

/**
 * Optional JSON snapshot of profile activity (heatmap + recent commits).
 * Set `RADICLE_ACTIVITY_SNAPSHOT_PATH` to an absolute path, or a path relative
 * to the Next.js process cwd (usually `dashboard/` when you run `npm start`).
 *
 * Generated by `scripts/export-activity-snapshot.mjs` on the machine whose
 * `radicle-httpd` has your latest commits (e.g. M1), then copy the file to M3.
 */
export async function readActivitySnapshot(): Promise<ActivityEntry[] | null> {
  const raw = process.env.RADICLE_ACTIVITY_SNAPSHOT_PATH?.trim();
  if (!raw) return null;
  const filePath = path.isAbsolute(raw)
    ? raw
    : path.join(process.cwd(), raw);
  try {
    const buf = await readFile(filePath, "utf8");
    const data = JSON.parse(buf) as SnapshotFile;
    if (!Array.isArray(data.entries)) return null;
    const entries = data.entries.filter(isActivityEntry);
    if (entries.length === 0) return null;
    return entries.sort(
      (a, b) => b.commit.committer.time - a.commit.committer.time,
    );
  } catch {
    return null;
  }
}
